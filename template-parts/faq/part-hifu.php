      HIFUはどれくらいの頻度で行うと効果が出ますか？
      </span>
    </div>
    <span class="p-faq__questionOpen"></span>
  </div>
  <div class="p-faq__answer" itemprop="acceptedAnswer" itemscope itemtype="https://schema.org/Answer">
